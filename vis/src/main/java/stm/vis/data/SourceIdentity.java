package stm.vis.data;

public class SourceIdentity {

	private String serviceId;
	private String serviceName;
	private String organisationMrn;
	private String organisationName;

	public String getServiceId() {
		return this.serviceId;
	}

	public void setServiceId(String serviceId) {
		this.serviceId = serviceId;
	}

	public String getServiceName() {
		return this.serviceName;
	}

	public void setServiceName(String serviceName) {
		this.serviceName = serviceName;
	}

	public String getOrganisationMrn() {
		return this.organisationMrn;
	}

	public void setOrganisationMrn(String organisationMrn) {
		this.organisationMrn = organisationMrn;
	}

	public String getOrganisationName() {
		return this.organisationName;
	}

	public void setOrganisationName(String organisationName) {
		this.organisationName = organisationName;
	}

}
